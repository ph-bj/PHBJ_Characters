const supplement = { ...manual, ...additions };

const src = readFileSync(join(ROOT, 'src/englishWorkTitles.ts'), 'utf8');
const existingMatch = src.match(
  /export const WORK_ENGLISH_BY_CHINESE: Record<string, string> = \{([\s\S]*?)\};/,
);
const existing = {};
for (const m of existingMatch[1].matchAll(/"([^"]+)":\s*"([^"]+)"/g)) {
  existing[m[1]] = m[2];
}

const merged = { ...existing };
for (const [zh, en] of Object.entries(supplement)) {
  if (!merged[zh]) merged[zh] = en;
}

const sortedKeys = Object.keys(merged).sort((a, b) => a.localeCompare(b, 'zh'));
const mapLines = sortedKeys.map((k) => `  "${k}": ${JSON.stringify(merged[k])},`).join('\n');

const titlesMatch = src.match(/export const ENGLISH_WORK_TITLES = \[([\s\S]*?)\] as const;/);
const titles = new Set();
for (const m of titlesMatch[1].matchAll(/"([^"]+)"/g)) titles.add(m[1]);
for (const en of Object.values(merged)) titles.add(en);
const titleLines = [...titles]
  .sort((a, b) => b.length - a.length)
  .map((t) => `  ${JSON.stringify(t)},`)
  .join('\n');

const supplementOnly = {};
for (const [zh, en] of Object.entries(supplement)) {
  if (!existing[zh]) supplementOnly[zh] = en;
}

let newSrc = src.replace(
  /import \{ WORK_ENGLISH_SUPPLEMENT \} from '\.\/workEnglishSupplement';\n\n/,
  '',
);
newSrc = newSrc
  .replace(
    /export const ENGLISH_WORK_TITLES = \[[\s\S]*?\] as const;/,
    `export const ENGLISH_WORK_TITLES = [\n${titleLines}\n] as const;`,
  )
  .replace(
    /export const WORK_ENGLISH_BY_CHINESE: Record<string, string> = \{[\s\S]*?\};/,
    `export const WORK_ENGLISH_BY_CHINESE: Record<string, string> = {\n${mapLines}\n};`,
  );

writeFileSync(join(ROOT, 'src/englishWorkTitles.ts'), newSrc);
console.log('Wrote supplement keys:', Object.keys(supplementOnly).length);
console.log('Total WORK_ENGLISH_BY_CHINESE keys:', sortedKeys.length);
